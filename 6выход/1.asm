;-----------------------------------------------------------------------------
; procedure Search(var Str1, Str2: array of byte; n, k: longword)
;-----------------------------------------------------------------------------
Search proc
;   [EBP+8]  - адрес Str1
;   [EBP+12] - адрес Str2
;   [EBP+16] - значение n
;   [EBP+20] - значение k
; далее ваше описание процедуры:
    push EBP
    mov EBP, ESP
    push EBX
    push ECX
    push EDX
    push EDI
    push ESI
    
    mov EBX, [EBP+12]
    mov EDX, [EBP+16]
    
    
    mov EAX, 0
    cld
@L: mov EDI, [EBP+12]
    add EDI, EAX
    inc EAX
    cmp EDX, 0
    jE @out
    dec EDX
    mov ESI, [EBP+8]
    mov ECX, [EBP+20]
repE cmpsb
    jnz @L
    
    mov EAX, EDI
    sub EAX, [EBP+12]
    jmp @e
@out: mov EAX, -1
@e: 
    pop EDI
    pop EDX
    pop ECX
    pop EBX
    pop EBP
    ret 4*4
Search endp
